// While

let index = 0
while (index <= 12) {
    console.log(`Value of index is ${index}`);
    index = index + 2
}



let myArray = ["Anshu", "Princess", "Shaktiman", "Thor", "Superman", "IronMan"]

let array = 0
while (array <= myArray.length) {
    console.log(`My Superhero is ${myArray[array]}`);
    array = array + 1
}



